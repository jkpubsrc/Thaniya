
import typing

import jk_typing

from .FileNamePattern import FileNamePattern













class ProcessingRule(object):

	################################################################################################################################
	## Constructors
	################################################################################################################################

	@jk_typing.checkFunctionSignature()
	def __init__(self, fileNamePattern:FileNamePattern, compressionAction:str):
		self.__fileNamePattern = fileNamePattern
		self.__compressionAction = compressionAction
	#

	################################################################################################################################
	## Public Property
	################################################################################################################################

	@property
	def fileNamePattern(self) -> FileNamePattern:
		return self.__fileNamePattern
	#

	@property
	def compressionAction(self) -> str:
		return self.__compressionAction
	#

	################################################################################################################################
	## Helper Method
	################################################################################################################################

	################################################################################################################################
	## Public Method
	################################################################################################################################

#










